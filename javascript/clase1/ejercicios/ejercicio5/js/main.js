const numeroUno = parseFloat(prompt("Ingrese el primero numero: "))
const numeroDos = parseFloat(prompt("Ingrese el segundo numero: "))
const numeroTres = parseFloat(prompt("Ingrese el tercer numero: "))

const resultado = ((numeroUno + numeroDos + numeroTres) / 3)

alert("El promedio de: " + numeroUno + " + " + numeroDos + " + " + numeroTres + " es: " + resultado)